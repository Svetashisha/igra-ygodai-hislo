import random

def дракон_маг_рыцарь():
    """Игра "Дракон, маг, рыцарь" """
    while True:
        выбор_компьютера = random.choice(["дракон", "маг", "рыцарь"])
        print("Выбери: дракон, маг или рыцарь (или 'выход' для завершения).")
        выбор_игрока = input("> ").lower()

        if выбор_игрока == 'выход':
            print("Спасибо за игру!")
            break

        print(f"Компьютер выбрал: {выбор_компьютера}")

        if выбор_игрока == выбор_компьютера:
            print("Ничья!")
        elif (выбор_игрока == "дракон" and выбор_компьютера == "маг") or \
             (выбор_игрока == "маг" and выбор_компьютера == "рыцарь") or \
             (выбор_игрока == "рыцарь" and выбор_компьютера == "дракон"):
            print("Ты выиграл!")
        else:
            print("Компьютер выиграл!")

дракон_маг_рыцарь()
